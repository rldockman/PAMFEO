module load BWA/0.7.17-GCCcore-11.3.0
module load SAMtools/1.16.1-GCC-11.3.0

##########################################################################################
# Script
##########################################################################################
bwa mem -t 64 -T 30 filteredcontigs.fa \
RawData/HiCPAMFEO_R1.fastq.gz|samtools view -b -@ 64 - > pam_f.bam 

samtools view -@ 64 -h pam_f.bam | perl otherScripts/filter_five_end.pl | samtools view -@ 64 -Sb - > filt_f.bam