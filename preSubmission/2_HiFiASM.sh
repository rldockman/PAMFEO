#!/bin/bash

##########################################################################################
# Notes
##########################################################################################
# Run hifiasm with hifi reads from all three cells concatenated, and with HiC fastq files

##########################################################################################
# Modules
##########################################################################################
module load hifiasm/0.19.6-GCCcore-11.3.0

##########################################################################################
# Script
##########################################################################################
hifiasm -o PAMFEO.asm -t 64 --h1 RawData/HiCPAMFEO_R1.fastq.gz \
--h2 RawData/HiCPAMFEO_R2.fastq.gz \
-f38 --primary RawData/PAMFEO_allruns.fastq.gz